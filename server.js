// ===== Init tables =====
async function initTables() {
  await pool.query(`
    CREATE TABLE IF NOT EXISTS books (
      id SERIAL PRIMARY KEY,
      name TEXT NOT NULL,
      author TEXT NOT NULL,
      category TEXT,
      position TEXT,
      created_at TIMESTAMP DEFAULT NOW()
    )
  `);

  await pool.query(`
    CREATE TABLE IF NOT EXISTS conversations (
      id SERIAL PRIMARY KEY,
      role TEXT NOT NULL,
      message TEXT NOT NULL,
      created_at TIMESTAMP DEFAULT NOW()
    )
  `);
}
await initTables();

// ===== Helpers =====
function extractFirstJson(text) {
  if (!text || typeof text !== "string") return null;
  const match = text.match(/\{[\s\S]*\}/);
  if (!match) return null;
  try {
    return JSON.parse(match[0]);
  } catch {
    return null;
  }
}

async function assignPosition(category) {
  if (!category) return "X?";
  const letter = category.trim()[0]?.toUpperCase() || "X";
  const res = await pool.query("SELECT COUNT(*) FROM books WHERE category = $1", [category]);
  const count = parseInt(res.rows[0].count || "0", 10);
  const shelf = Math.floor(count / 15) + 1;
  return `${letter}${shelf}`;
}

async function inferCategory(bookName, author) {
  const model = genAI.getGenerativeModel({ model: "gemini-2.5-flash" });

  const prompt = `
B·∫°n l√† qu·∫£n th·ªß th∆∞ vi·ªán th√¥ng minh.
D·ª±a tr√™n t√™n s√°ch v√† t√°c gi·∫£, h√£y x√°c ƒë·ªãnh TH·ªÇ LO·∫†I ph√π h·ª£p nh·∫•t.

T√™n: "${bookName}"
T√°c gi·∫£: "${author}"

Tr·∫£ v·ªÅ JSON duy nh·∫•t: {"category": "Th·ªÉ lo·∫°i"}
`;

  try {
    const result = await model.generateContent({
      contents: [{ role: "user", parts: [{ text: prompt }] }]
    });
    const raw = result.response.text();
    const parsed = extractFirstJson(raw);
    if (parsed && parsed.category) return parsed.category;

    const titleLower = bookName.toLowerCase();
    if (/(python|program|code|data|ai|machine)/i.test(titleLower)) return "C√¥ng ngh·ªá";
    if (/(ti·ªÉu thuy·∫øt|truy·ªán|novel|poem|k√Ω)/i.test(titleLower)) return "VƒÉn h·ªçc";
    if (/(l·ªãch s·ª≠|history|war|chi·∫øn tranh)/i.test(titleLower)) return "L·ªãch s·ª≠";
    return "Ch∆∞a r√µ";
  } catch {
    return "Ch∆∞a r√µ";
  }
}

